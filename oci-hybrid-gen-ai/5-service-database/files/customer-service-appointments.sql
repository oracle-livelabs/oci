    CASE MOD(LEVEL, 10)
        WHEN 0 THEN 'Oil and filter replacement'
        WHEN 1 THEN 'Tire rotation'
        WHEN 2 THEN 'Multi-point inspection'
        WHEN 3 THEN 'Brake pad replacement'
        WHEN 4 THEN 'Diagnostic scan'
        WHEN 5 THEN 'Battery test and replacement'
        WHEN 6 THEN 'Software update'
        WHEN 7 THEN 'Fluid exchange'
        WHEN 8 THEN 'Air filter replacement'
        ELSE 'Road test and quality check'
    END AS description,
    25 + (MOD(LEVEL, 6) * 20) AS labor_cost,
    CASE MOD(LEVEL, 10)
        WHEN 0 THEN 38
        WHEN 3 THEN 110
        WHEN 5 THEN 165
        WHEN 7 THEN 48
        WHEN 8 THEN 28
        ELSE 0
    END AS parts_cost
FROM dual
CONNECT BY LEVEL <= 100;

