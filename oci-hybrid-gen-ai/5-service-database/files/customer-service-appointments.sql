CREATE TABLE customers (
    customer_id    NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    full_name      VARCHAR2(200) NOT NULL,
    email          VARCHAR2(320),
    phone          VARCHAR2(40),
    CONSTRAINT customers_pk PRIMARY KEY (customer_id)
);

CREATE TABLE vehicles (
    vehicle_id     NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    customer_id    NUMBER NOT NULL,
    vin            VARCHAR2(17) NOT NULL,
    make           VARCHAR2(100),
    model          VARCHAR2(100),
    model_year     NUMBER(4),
    CONSTRAINT vehicles_pk PRIMARY KEY (vehicle_id),
    CONSTRAINT vehicles_customer_fk FOREIGN KEY (customer_id)
        REFERENCES customers (customer_id),
    CONSTRAINT vehicles_vin_uk UNIQUE (vin)
);

CREATE TABLE service_appointments (
    appointment_id          NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    customer_id             NUMBER NOT NULL,
    vehicle_id              NUMBER NOT NULL,
    received_at             TIMESTAMP WITH TIME ZONE NOT NULL,
    completed_at            TIMESTAMP WITH TIME ZONE,
    odometer_miles          NUMBER(10),
    service_summary         CLOB,
    warranty_type           VARCHAR2(30) DEFAULT 'NONE' NOT NULL,
    total_cost              NUMBER(12,2) DEFAULT 0 NOT NULL,
    warranty_covered_amount NUMBER(12,2) DEFAULT 0 NOT NULL,
    customer_paid_amount    NUMBER(12,2) DEFAULT 0 NOT NULL,
    CONSTRAINT service_appointments_pk PRIMARY KEY (appointment_id),
    CONSTRAINT service_appt_customer_fk FOREIGN KEY (customer_id)
        REFERENCES customers (customer_id),
    CONSTRAINT service_appt_vehicle_fk FOREIGN KEY (vehicle_id)
        REFERENCES vehicles (vehicle_id),
    CONSTRAINT service_appt_warranty_ck CHECK (
        warranty_type IN ('NONE', 'MANUFACTURER', 'EXTENDED', 'RECALL')
    ),
    CONSTRAINT service_appt_amounts_ck CHECK (
        total_cost >= 0
        AND warranty_covered_amount >= 0
        AND customer_paid_amount >= 0
    )
);

CREATE TABLE service_items (
    service_item_id  NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    appointment_id   NUMBER NOT NULL,
    description      VARCHAR2(1000) NOT NULL,
    labor_cost       NUMBER(12,2) DEFAULT 0 NOT NULL,
    parts_cost       NUMBER(12,2) DEFAULT 0 NOT NULL,
    CONSTRAINT service_items_pk PRIMARY KEY (service_item_id),
    CONSTRAINT service_items_appt_fk FOREIGN KEY (appointment_id)
        REFERENCES service_appointments (appointment_id)
        ON DELETE CASCADE,
    CONSTRAINT service_items_amounts_ck CHECK (
        labor_cost >= 0
        AND parts_cost >= 0
    )
);

CREATE INDEX vehicles_customer_ix
    ON vehicles (customer_id);

CREATE INDEX service_appt_vehicle_ix
    ON service_appointments (vehicle_id);

CREATE INDEX service_items_appt_ix
    ON service_items (appointment_id);

