INSERT INTO service_appointments (
    appointment_id,
    customer_id,
    vehicle_id,
    received_at,
    completed_at,
    odometer_miles,
    service_summary,
    warranty_type,
    total_cost,
    warranty_covered_amount,
    customer_paid_amount
)
SELECT
    appointment_id,
    customer_id,
    vehicle_id,
    received_at,
    completed_at,
    odometer_miles,
    service_summary,
    warranty_type,
    total_cost,
    warranty_covered_amount,
    total_cost - warranty_covered_amount
FROM (
    SELECT
        LEVEL AS appointment_id,
        MOD(LEVEL - 1, 10) + 1 AS customer_id,
        MOD(LEVEL - 1, 10) + 1 AS vehicle_id,
        FROM_TZ(TIMESTAMP '2025-01-06 08:00:00', '-08:00') + NUMTODSINTERVAL(LEVEL * 9, 'DAY') AS received_at,
        FROM_TZ(TIMESTAMP '2025-01-06 16:30:00', '-08:00') + NUMTODSINTERVAL(LEVEL * 9, 'DAY') AS completed_at,
        18000 + (LEVEL * 1375) AS odometer_miles,
        CASE MOD(LEVEL, 6)
            WHEN 0 THEN 'Oil change, tire rotation, and multi-point inspection'
            WHEN 1 THEN 'Brake inspection and brake component replacement'
            WHEN 2 THEN 'Engine diagnostic and sensor replacement'
            WHEN 3 THEN 'Battery, charging system, and electrical inspection'
            WHEN 4 THEN 'Recall service and software update'
            ELSE 'Scheduled maintenance and fluid service'
        END AS service_summary,
        CASE
            WHEN MOD(LEVEL, 10) IN (3, 7) THEN 'MANUFACTURER'
            WHEN MOD(LEVEL, 10) IN (4, 8) THEN 'EXTENDED'
            WHEN MOD(LEVEL, 10) = 5 THEN 'RECALL'
            ELSE 'NONE'
        END AS warranty_type,
        150 + (MOD(LEVEL, 8) * 75) AS total_cost,
        CASE
            WHEN MOD(LEVEL, 10) IN (3, 5, 7) THEN 150 + (MOD(LEVEL, 8) * 75)
            WHEN MOD(LEVEL, 10) IN (4, 8) THEN ROUND((150 + (MOD(LEVEL, 8) * 75)) * 0.70, 2)
            ELSE 0
        END AS warranty_covered_amount
    FROM dual
    CONNECT BY LEVEL <= 30
);

